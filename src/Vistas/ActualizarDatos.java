    private void jbModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbModificarActionPerformed
        // TODO add your handling code here:
        if(lector == null){
            JOptionPane.showMessageDialog(this, "Busque un socio por su DNI para modificarlo");
        }else{
            String dni = jtfDniLector.getText();
            String nombre = jtfNombre.getText(); 
            String domicilio = jtfDomicilio.getText();
            String telefono = jtfTelefono.getText();
            boolean estado = jrbActivo.isSelected();
            String mail = jtfMail.getText();

            if(dni.isEmpty() || nombre.isEmpty() || domicilio.isEmpty() || telefono.isEmpty() || mail.isEmpty()){
                JOptionPane.showMessageDialog(this, "No debe haber campos vacíos");
            } else{
                lector.setDni(dni);
                lector.setNombre(nombre);
                lector.setDomicilio(domicilio);
                lector.setTelefono(telefono);
                lector.isEstado();
                lector.setMail(mail);
                ld.modificarLector(lector);
                    
                //Seteo de campos con los valores actualizados
                //int idLector = lector.getIdLector();
                lector = ld.buscarLectorPorDni(dni);
                jtfNombre.setText(lector.getNombre()+"");
                jtfDomicilio.setText(lector.getDomicilio());
                jtfTelefono.setText(lector.getTelefono());
                if (lector.isEstado()){
                    jrbActivo.setSelected(true);
                } else{
                    jrbActivo.setSelected(false);
                }
                jtfMail.setText(lector.getMail());
            }
        }
    }//GEN-LAST:event_jbModificarActionPerformed

    private void jbAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAltaActionPerformed
        // TODO add your handling code here:
        if(lector!= null){
            ld.activarLector(lector.getIdLector());
            if (lector.isEstado()){
                    jrbActivo.setSelected(true);
                } else{
                    jrbActivo.setSelected(false);
                }
        }else{
            JOptionPane.showMessageDialog(this, "No hay un socio seleccionado");    
        }
    }//GEN-LAST:event_jbAltaActionPerformed

    private void jbBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBajaActionPerformed
        // TODO add your handling code here:
        if(lector!= null){
            ld.desactivarLector(lector.getIdLector());
            if (lector.isEstado()){
                    jrbActivo.setSelected(false);
                } else{
                    jrbActivo.setSelected(true);
                }
        }else{
            JOptionPane.showMessageDialog(this, "No hay un socio seleccionado");    
        }
    }//GEN-LAST:event_jbBajaActionPerformed

