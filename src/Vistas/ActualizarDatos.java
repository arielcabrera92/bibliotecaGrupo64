    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        // TODO add your handling code here:
        try{
            Integer dni = Integer.parseInt(jtfDniLector.getText());
            String dniString = dni.toString();
            lector = ld.buscarLectorPorDni(dniString);
            if(lector != null){
                jtfNombre.setText(lector.getNombre()+"");
                jtfDomicilio.setText(lector.getDomicilio());
                jtfTelefono.setText(lector.getTelefono());
                jtfMail.setText(lector.getMail());
                if (lector.isEstado()){
                    jrbActivo.setSelected(true);
                } else{
                    jrbActivo.setSelected(false);
                }
            }
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(this, "Debe ingresar un numero valido");
        }
    }//GEN-LAST:event_jbBuscarActionPerformed

