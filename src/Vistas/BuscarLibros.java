    private void armarCabeceraTabla(){
        ArrayList <Object> filaCabecera = new ArrayList<>();
        filaCabecera.add("ID Libro");
        filaCabecera.add("ISBN");
        filaCabecera.add("Nombre");
        filaCabecera.add("Tipo");
        filaCabecera.add("Editorial");
        filaCabecera.add("Autor");
        filaCabecera.add("Estado");
        filaCabecera.add("AÃ±o");
        filaCabecera.add("Ejemplares"); 
        filaCabecera.add("Disponibilidad");
        for(Object it: filaCabecera){
            modelo.addColumn(it);
        }
        jtLibros.setModel(modelo);
    }
    private void cargarLibrosNombre(){
        borrarFilasTabla();
        String nombre = jtfBuscarXNombre.getText().trim(); // Elimina espacios en blanco alrededor del nombre
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar el nombre del libro");
        } else {
            try {
                List<Libro> lista = (ArrayList) ld.buscarLibroPorNombre(nombre);
                for (Libro libro : lista) {
                    modelo.addRow(new Object[] {libro.getIdLibro(), libro.getIsbn(), libro.getNombre(), libro.getTipo(), libro.getEditorial(), libro.getAutor(), libro.isEstado(), libro.getAnio(), libro.getEjemplares(), libro.getDisponibilidad()});
                }
            } catch (Exception ex) {
                // Manejo de otras excepciones, si es necesario
                JOptionPane.showMessageDialog(this, "Ha ocurrido un error: " + ex.getMessage());
            }
        }
    }
    private void cargarLibrosAutor(){
        borrarFilasTabla();
        String autor = jtfBuscarXAutor.getText().trim(); // Elimina espacios en blanco alrededor del nombre
        if (autor.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar el nombre del autor");
        } else {
            try {
                List<Libro> lista = (ArrayList) ld.buscarLibroPorAutor(autor);
                for (Libro libro : lista) {
                    modelo.addRow(new Object[] {libro.getIdLibro(), libro.getIsbn(), libro.getNombre(), libro.getTipo(), libro.getEditorial(), libro.getAutor(), libro.isEstado(), libro.getAnio(), libro.getEjemplares(), libro.getDisponibilidad()});
                }
            } catch (Exception ex) {
                
                JOptionPane.showMessageDialog(this, "Ha ocurrido un error: " + ex.getMessage());
            }
        }
    }
    private void borrarFilasTabla(){
        int indice = modelo.getRowCount() -1;
        for (int i = indice; i>=0; i--){
            modelo.removeRow(i);
        }
    }
