    private void jbAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarActionPerformed
        // TODO add your handling code here:
        try{
            Integer isbn = Integer.parseInt(jtfIsbn.getText()); 
            String nombre = jtfNombre.getText();
            String tipo = jtfTipo.getText();
            String editorial = jtfEditorial.getText(); 
            String autor = jtfAutor.getText();
            Boolean estado = jrbEstado.isSelected(); 
            Integer anio = Integer.parseInt(jtfAnio.getText()); 
            Integer ejemplares = Integer.parseInt(jtfEjemplares.getText());
            EstadoEjemplar disponibilidad = EstadoEjemplar.DISPONIBLE;
            
            if(nombre.isEmpty()|| tipo.isEmpty() || editorial.isEmpty() || autor.isEmpty()){
                JOptionPane.showMessageDialog(this, "No debe haber campos vacios");
            return;
            }
            if(libro == null){
       
                libro = new Libro (isbn, nombre, tipo, editorial, autor, estado, anio, ejemplares, disponibilidad);
                ld.guardarLibro(libro);
            }else{
                JOptionPane.showMessageDialog(this, "El libro ya estÃ¡ registrado");
                
            }
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(this, "Debe ingresar nÃºmeros enteros en los campos numÃ©ricos");
        } 
    }//GEN-LAST:event_jbAgregarActionPerformed

